class WhileLoop {
    /*
     * A while loop statement repeatedly executes a target statement as long as a
     * given condition is true.
     */
    public static void main(String[] args) {
        int x = 6;

        while (x < 10) {
            System.out.println(x);
            x++;
        }

        System.out.println("Loop ended");
    }
}
